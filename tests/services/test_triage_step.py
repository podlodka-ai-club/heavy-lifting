def test_triage_step_prompt_includes_role_and_task_context() -> None:
    runner = _FakeAgentRunner(canned_stdout="")  # not used here
    step = TriageStep(agent_runner=runner, triage_prompt_text=_TRIAGE_PROMPT_FIXTURE)

    available_context = _make_task_context(
        title="Add X-Trace-Id middleware",
        description="Сквозной trace id во всех ответах backend.",
        acceptance_criteria=["middleware покрыт unit-тестом"],
        repo_url="https://example.test/repo.git",
        repo_ref="main",
        workspace_key="repo-x",
    )
    rendered_available = step.build_prompt(task_context=available_context)

    assert "<role>" in rendered_available
    assert "</role>" in rendered_available
    assert "ROLE: Triage Gatekeeper" in rendered_available

    assert "<task_context>" in rendered_available
    assert "title: Add X-Trace-Id middleware" in rendered_available
    assert "description: Сквозной trace id во всех ответах backend." in rendered_available
    assert "- middleware покрыт unit-тестом" in rendered_available

    assert "<repo_signals>" in rendered_available
    assert "repo_url: https://example.test/repo.git" in rendered_available
    assert "repo_available: true" in rendered_available

    assert "<expected_output>" in rendered_available

    missing_context = _make_task_context(
        repo_url=None,
        repo_ref="main",
        workspace_key="repo-x",
    )
    rendered_missing = step.build_prompt(task_context=missing_context)
    assert "repo_available: false" in rendered_missing


def test_triage_step_passes_built_prompt_to_runner() -> None:
    """Critical: the triage prompt must reach the runner via prompt_override.

    Without this, ``CliAgentRunner`` would fall back to its generic
    ``_build_prompt`` and the triage agent would never see the
    ``<role>/<task_context>/<repo_signals>/<expected_output>`` contract.
    """

    canned = _envelope(
        story_points=2,
        task_kind="implementation",
        outcome="routed",
        markdown_body=_HANDOVER_BRIEF_BODY.format(sp=2),
    )
    runner = _FakeAgentRunner(canned_stdout=canned)
    step = TriageStep(agent_runner=runner, triage_prompt_text=_TRIAGE_PROMPT_FIXTURE)

    step.run(
        task_context=_make_task_context(),
        workspace_path="/tmp/workspace/repo-x",
    )

    assert runner.last_request is not None
    prompt = runner.last_request.prompt_override
    assert prompt is not None, "TriageStep.run must set AgentRunRequest.prompt_override"
    assert "<role>" in prompt
    assert "ROLE: Triage Gatekeeper" in prompt  # from _TRIAGE_PROMPT_FIXTURE
    assert "<task_context>" in prompt
    assert "title: Add X-Trace-Id middleware" in prompt
    assert "<repo_signals>" in prompt
    assert "repo_available: true" in prompt
    assert "<expected_output>" in prompt
