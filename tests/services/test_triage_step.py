def _envelope(
    *,
    story_points: int,
    task_kind: str,
    outcome: str,
    markdown_body: str,
) -> str:
    return (
        "<triage_result>\n"
        f"story_points: {story_points}\n"
        f"task_kind: {task_kind}\n"
        f"outcome: {outcome}\n"
        "</triage_result>\n"
        "<markdown>\n"
        f"{markdown_body}"
        "</markdown>\n"
    )


@dataclass(slots=True)
class _FakeAgentRunner:
    """Minimal :class:`AgentRunnerProtocol` test double."""

    canned_stdout: str
    parsed_stdout: str | None = None
    last_request: AgentRunRequest | None = None

    def run(self, request: AgentRunRequest) -> AgentRunResult:
        self.last_request = request
        return AgentRunResult(
            payload=TaskResultPayload(summary="fake", token_usage=[]),
            raw_stdout=self.canned_stdout,
            raw_stderr="",
            parsed_stdout=self.parsed_stdout,
        )


def _make_chain_entry(
    *,
    task_id: int,
    task_type: TaskType,
    context: TaskContext | None = None,
) -> TaskChainEntry:
    """Build a transient ``TaskChainEntry`` without involving the DB."""

    task = Task(id=task_id, task_type=task_type)
    entry = TaskChainEntry(task=task)
    object.__setattr__(entry, "context", context)
    object.__setattr__(entry, "input_payload", None)
    object.__setattr__(entry, "result_payload", None)
    return entry


def _make_task_context(
    *,
    title: str = "Add X-Trace-Id middleware",
    description: str | None = "Сквозной trace id во всех ответах backend.",
    acceptance_criteria: list[str] | None = None,
    repo_url: str | None = "https://example.test/repo.git",
    repo_ref: str | None = "main",
    workspace_key: str | None = "repo-x",
) -> EffectiveTaskContext:
    """Compose a minimal :class:`EffectiveTaskContext` for triage tests."""

    if acceptance_criteria is None:
        acceptance_criteria = [
            "middleware покрыт unit-тестом",
            "заголовок присутствует во всех ответах",
        ]

    tracker_context = TaskContext(
        title=title,
        description=description,
        acceptance_criteria=acceptance_criteria,
    )

    fetch_entry = _make_chain_entry(
        task_id=1,
        task_type=TaskType.FETCH,
        context=tracker_context,
    )
    execute_entry = _make_chain_entry(
        task_id=2,
        task_type=TaskType.EXECUTE,
        context=tracker_context,
    )

    return EffectiveTaskContext(
        flow_type=TaskType.EXECUTE,
        root_task=fetch_entry,
        current_task=execute_entry,
        lineage=(fetch_entry, execute_entry),
        fetch_task=fetch_entry,
        execute_task=execute_entry,
        deliver_task=None,
        feedback_task=None,
        feedback_history=(),
        repo_url=repo_url,
        repo_ref=repo_ref,
        workspace_key=workspace_key,
        branch_name=None,
        base_branch=None,
        pr_external_id=None,
        pr_url=None,
        instructions=None,
        commit_message_hint=None,
        handover_brief=None,
    )


