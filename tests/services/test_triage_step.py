def test_triage_step_sp1_builds_routed_payload() -> None:
    canned = _envelope(
        story_points=1,
        task_kind="implementation",
        outcome="routed",
        markdown_body=_HANDOVER_BRIEF_BODY.format(sp=1),
    )
    runner = _FakeAgentRunner(canned_stdout=canned)
    step = TriageStep(agent_runner=runner, triage_prompt_text=_TRIAGE_PROMPT_FIXTURE)

    result = step.run(
        task_context=_make_task_context(),
        workspace_path="/tmp/workspace/repo-x",
    )

    assert isinstance(result, TriageStepResult)
    payload = result.result_payload
    assert payload.outcome == "routed"
    assert payload.summary.startswith("Triage завершён: SP=1")

    assert payload.classification is not None
    assert payload.classification.task_kind == "implementation"

    assert payload.estimate is not None
    assert payload.estimate.story_points == 1
    assert payload.estimate.complexity == "trivial"
    assert payload.estimate.can_take_in_work is True
    assert payload.estimate.blocking_reasons == []

    assert payload.routing is not None
    assert payload.routing.create_followup_task is True
    assert payload.routing.next_role == "implementation"
    assert payload.routing.next_task_type == "execute"
    assert payload.routing.requires_human_approval is False

    assert payload.delivery is not None
    assert payload.delivery.tracker_status is None
    assert payload.delivery.tracker_estimate == 1
    assert payload.delivery.tracker_labels == ["sp:1", "triage:ready"]
    assert payload.delivery.escalation_kind is None
    assert payload.delivery.comment_body == "Triage SP=1: Brief сохранён, передано в работу."

    handover = payload.metadata.get("handover_brief")
    assert isinstance(handover, str)
    assert handover.startswith("## Agent Handover Brief")

    assert payload.tracker_comment is None


