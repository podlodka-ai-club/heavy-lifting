"""Unit tests for ``backend.services.triage_parser``.

The triage agent stdout contract is defined in ``prompts/agents/triage.md``.
These tests validate the parser deterministically by hand-building the
``<triage_result>`` / ``<markdown>`` envelope — no live LLM is involved.
"""

