def test_parse_raises_when_no_triage_result_block() -> None:
    raw = "<markdown>\n## Agent Handover Brief\nbody\n</markdown>\n"

    with pytest.raises(TriageOutputError) as excinfo:
        parse_triage_output(raw)

    assert "exactly one" in str(excinfo.value)


def test_parse_raises_when_two_triage_result_blocks() -> None:
    block_one = (
        "<triage_result>\n"
        "story_points: 1\n"
        "task_kind: implementation\n"
        "outcome: routed\n"
        "</triage_result>\n"
    )
    block_two = (
        "<triage_result>\n"
        "story_points: 2\n"
        "task_kind: implementation\n"
        "outcome: routed\n"
        "</triage_result>\n"
    )
    raw = (
        block_one
        + block_two
        + "<markdown>\n"
        + _HANDOVER_BRIEF_BODY.format(sp=1)
        + "</markdown>\n"
    )

    with pytest.raises(TriageOutputError) as excinfo:
        parse_triage_output(raw)

    assert "exactly one" in str(excinfo.value)


