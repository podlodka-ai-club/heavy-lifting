def test_execute_worker_defaults_to_implementation_when_action_missing(tmp_path) -> None:
    """Legacy execute-задачи без явного ``action`` обязаны идти по impl-flow.

    До миграции worker всегда уходил в impl-flow: любая уже существующая в
    БД single-execute-задача должна остаться там же, иначе после rollout
    повторно поднятые legacy-задачи внезапно уйдут в triage и не получат
    branch/PR. Триаж назначается **только** через явный
    ``action="triage"`` от ``tracker_intake`` (task08).
    """

    session_factory = _build_session_factory(tmp_path)
    settings = _build_settings(tmp_path)
    agent_runner = RecordingAgentRunner()
    worker = ExecuteWorker(
        scm=MockScm(),
        agent_runner=agent_runner,
        session_factory=session_factory,
        settings=settings,
    )

    with session_scope(session_factory=session_factory) as session:
        repository = TaskRepository(session)
        fetch = _create_fetch(repository, suffix="2")
        execute = repository.create_task(
            TaskCreateParams(
                task_type=TaskType.EXECUTE,
                parent_id=fetch.id,
                tracker_name="mock",
                external_parent_id="TASK-2",
                repo_url="https://example.test/repo.git",
                repo_ref="main",
                workspace_key="repo-2",
                context={"title": "Legacy execute"},
                input_payload={  # no action key — should default to impl-flow
                    "instructions": "Run.",
                    "branch_name": "task2/run",
                    "base_branch": "main",
                },
            )
        )
        execute_id = execute.id

    report = worker.poll_once()

    assert report.processed_execute_tasks == 1
    assert len(agent_runner.requests) == 1

    with session_scope(session_factory=session_factory) as session:
        execute_row = session.get(Task, execute_id)
        assert execute_row is not None
        assert execute_row.status == TaskStatus.DONE
        # impl-flow создаёт branch + PR — это доказывает, что мы НЕ ушли в triage.
        assert execute_row.branch_name == "task2/run"
        assert execute_row.pr_external_id is not None


