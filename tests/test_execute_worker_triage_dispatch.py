def test_execute_worker_lazy_loads_triage_prompt_once(tmp_path, monkeypatch) -> None:
    session_factory = _build_session_factory(tmp_path)
    settings = _build_settings(tmp_path)
    fake = _FakeAgentRunner(
        canned_stdout=_envelope(
            story_points=2,
            task_kind="implementation",
            outcome="routed",
            markdown_body=_HANDOVER_BRIEF_BODY,
        )
    )
    worker = ExecuteWorker(
        scm=MockScm(),
        agent_runner=fake,
        session_factory=session_factory,
        settings=settings,
    )

    call_counter = {"count": 0}
    from backend.workers import execute_worker as worker_module

    original_load = worker_module.load_triage_prompt

    def counting_load(prompts_dir):
        call_counter["count"] += 1
        return original_load(prompts_dir)

    monkeypatch.setattr(worker_module, "load_triage_prompt", counting_load)

    with session_scope(session_factory=session_factory) as session:
        repository = TaskRepository(session)
        for idx in range(2):
            fetch = _create_fetch(repository, suffix=f"9-{idx}")
            repository.create_task(
                TaskCreateParams(
                    task_type=TaskType.EXECUTE,
                    parent_id=fetch.id,
                    tracker_name="mock",
                    external_parent_id=f"TASK-9-{idx}",
                    repo_url="https://example.test/repo.git",
                    repo_ref="main",
                    workspace_key=f"repo-9-{idx}",
                    context={"title": "Triage execute"},
                    input_payload={"action": "triage"},
                )
            )

    worker.poll_once()
    worker.poll_once()

    assert worker._triage_prompt_text is not None
    assert call_counter["count"] == 1


