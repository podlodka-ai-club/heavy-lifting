def _build_session_factory(tmp_path):
    engine = build_engine(f"sqlite+pysqlite:///{tmp_path / 'app.db'}")
    Base.metadata.create_all(engine)
    return build_session_factory(engine)


def _build_settings(tmp_path: Path) -> Settings:
    """Settings with an isolated workspace_root and prompts_dir under tmp_path.

    The triage prompt body is irrelevant for these tests (TriageStep injects it
    verbatim into the prompt and the fake runner ignores the prompt) — we only
    need ``load_triage_prompt`` to find a non-empty file at
    ``<prompts_dir>/agents/triage.md``.
    """

    prompts_dir = tmp_path / "prompts"
    (prompts_dir / "agents").mkdir(parents=True, exist_ok=True)
    (prompts_dir / "agents" / "triage.md").write_text(
        "ROLE: Triage Gatekeeper. Goal: classify ticket and emit Story Point.",
        encoding="utf-8",
    )
    return replace(
        get_settings(),
        workspace_root=str(tmp_path / "workspaces"),
        prompts_dir=str(prompts_dir),
    )


def _create_fetch(repository: TaskRepository, *, with_repo: bool = True, suffix: str = "1") -> Task:
    if with_repo:
        return repository.create_task(
            TaskCreateParams(
                task_type=TaskType.FETCH,
                tracker_name="mock",
                external_task_id=f"TASK-{suffix}",
                repo_url="https://example.test/repo.git",
                repo_ref="main",
                workspace_key=f"repo-{suffix}",
                context={"title": "Tracker task"},
            )
        )
    return repository.create_task(
        TaskCreateParams(
            task_type=TaskType.FETCH,
            tracker_name="mock",
            external_task_id=f"TASK-{suffix}",
            context={"title": "Tracker task without repo"},
        )
    )


# ---------------------------------------------------------------------------
# Test 1 — explicit action="triage" → TriageStep.run with full DONE flow
# ---------------------------------------------------------------------------


def test_execute_worker_routes_triage_action_through_triage_step(tmp_path) -> None:
    session_factory = _build_session_factory(tmp_path)
    settings = _build_settings(tmp_path)
    fake = _FakeAgentRunner(
        canned_stdout=_envelope(
            story_points=2,
            task_kind="implementation",
            outcome="routed",
            markdown_body=_HANDOVER_BRIEF_BODY,
        )
    )
    worker = ExecuteWorker(
        scm=MockScm(),
        agent_runner=fake,
        session_factory=session_factory,
        settings=settings,
    )

    with session_scope(session_factory=session_factory) as session:
        repository = TaskRepository(session)
        fetch = _create_fetch(repository, suffix="1")
        triage_task = repository.create_task(
            TaskCreateParams(
                task_type=TaskType.EXECUTE,
                parent_id=fetch.id,
                tracker_name="mock",
                external_parent_id="TASK-1",
                repo_url="https://example.test/repo.git",
                repo_ref="main",
                workspace_key="repo-1",
                context={"title": "Triage execute"},
                input_payload={"action": "triage"},
            )
        )
        triage_id = triage_task.id

    report = worker.poll_once()

    assert report.processed_execute_tasks == 1
    assert report.failed_execute_tasks == 0
    assert len(fake.requests) == 1
    request = fake.requests[0]
    assert request.workspace_path == "/tmp/mock-scm/repo-1"
    assert request.metadata["action"] == "triage"

    with session_scope(session_factory=session_factory) as session:
        triage_row = session.get(Task, triage_id)
        assert triage_row is not None
        assert triage_row.status == TaskStatus.DONE
        assert triage_row.branch_name is None
        assert triage_row.pr_external_id is None
        assert triage_row.pr_url is None
        assert triage_row.result_payload is not None
        assert triage_row.result_payload["estimate"]["story_points"] == 2
        assert triage_row.result_payload["delivery"]["tracker_labels"] == ["sp:2", "triage:ready"]
        handover = triage_row.result_payload["metadata"].get("handover_brief")
        assert isinstance(handover, str)
        assert handover.startswith("## Agent Handover Brief")

        deliver = (
            session.query(Task)
            .filter(Task.parent_id == triage_id, Task.task_type == TaskType.DELIVER)
            .first()
        )
        assert deliver is not None
        assert deliver.status == TaskStatus.NEW


