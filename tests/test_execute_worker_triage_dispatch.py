_HANDOVER_BRIEF_BODY = """## Agent Handover Brief
**Assigned Story Points:** 2
**Generated by:** AI Triage Gatekeeper

### 1. Intent
- Цель: добавить middleware.

### 2. Execution Design
1. Реализовать middleware.

### 3. Architectural Constraints
- Покрыть unit-тестом.

### 4. Context
- src/backend/app.py

### 5. Edge Cases
- Конкурентные запросы.

### 6. Acceptance Signals
- [ ] middleware покрыт unit-тестом.
"""


def _envelope(
    *,
    story_points: int,
    task_kind: str,
    outcome: str,
    markdown_body: str,
) -> str:
    return (
        "<triage_result>\n"
        f"story_points: {story_points}\n"
        f"task_kind: {task_kind}\n"
        f"outcome: {outcome}\n"
        "</triage_result>\n"
        "<markdown>\n"
        f"{markdown_body}"
        "</markdown>\n"
    )


@dataclass(slots=True)
class _FakeAgentRunner:
    """Minimal AgentRunnerProtocol double — returns canned stdout."""

    canned_stdout: str
    last_request: AgentRunRequest | None = None
    requests: list[AgentRunRequest] = field(default_factory=list)

    def run(self, request: AgentRunRequest) -> AgentRunResult:
        self.last_request = request
        self.requests.append(request)
        return AgentRunResult(
            payload=TaskResultPayload(summary="fake", token_usage=[]),
            raw_stdout=self.canned_stdout,
            raw_stderr="",
        )


